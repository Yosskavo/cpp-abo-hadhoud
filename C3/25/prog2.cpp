#include <iostream>

using namespace std;

int main (void)
{
    cout << "(5 > 6 && 7 == 7) || (1 || 0)                       = " << ((5 > 6 && 7 == 7) || (1 || 0)) << endl;
    cout << "!(5 > 6 && 7 == 7) || (1 || 0)                      = " << (!(5 > 6 && 7 == 7) || (1 || 0)) << endl;
    cout << "!(5 > 6 && 7 == 7) || !(1 || 0)                     = " << (!(5 > 6 && 7 == 7) || !(1 || 0)) << endl;
    cout << "!(5 > 6 && 7 == 7) && !(1 || 0)                     = " << (!(5 > 6 && 7 == 7) && !(1 || 0)) << endl;
    cout << "((5 > 6 && 7 <= 8) || (8 > 1 && 4 <= 3)) && true    = " << (((5 > 6 && 7 <= 8) || (8 > 1 && 4 <= 3)) && true) << endl;
    cout << "((5 > 6 && !(7 <= 8)) || (8 > 1 || 4 <= 3)) || true = " << (((5 > 6 && !(7 <= 8)) || (8 > 1 || 4 <= 3)) || true) << endl;
}